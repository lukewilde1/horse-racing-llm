CREATE TABLE IF NOT EXISTS races (
    "Date" TEXT,
    "Track" TEXT,
    "Race Number" INTEGER,
    "Horse" TEXT,
    "Barrier" INTEGER,
    "Jockey" TEXT,
    "Trainer" TEXT,
    "SP" REAL,
    "Finish Position" INTEGER,
    "Race Distance" INTEGER,
    "Class" TEXT,
    "Track Condition" TEXT,
    "Weather" TEXT,
    "Total Runners" INTEGER
);


